// format
// pos, color, size

function putFlower(petalcolor=[1,1,1,1])
{
    let reposX = Math.random() - randomRange(0,1);
    let reposZ = Math.random() - randomRange(0,1);
    // color of pollen area
    let innerColor = [1, 0.9098039216, 0.3254901961, 1];

    // creates a "circle", inside a circle
    let default_positions = [
        // 6 vertices
        [0,     -0.49,    0,      1],
        [-0.02, -0.49,    0.01,   1],
        [-0.01, -0.49,    0.015,  1],
        [0,     -0.49,    0.02,   1],
        [0.01, -0.49,     0.015,  1],
        [0.02, -0.49,     0.01,  1],
        [0,    -0.49,     0,  1],
    ];

    let size = randomRange(2,4);

    // flower points of 

    let flowerInner = [];
    default_positions.forEach(element => {
        let empty = [];
        // insert color into said element and modify position
        InsertData(empty, element);
        empty[0] += reposX;
        empty[2] += reposZ;
        InsertData(empty, innerColor);
        InsertData(flowerInner, empty);
        flowerInner.push(1);
    });

    // outer
    let flowerOuter = [];
    let i = 0;  // to access flower inner 
    default_positions.forEach(element => {
        // multiply the default pos with randomly decided size and move to the position
        let empty= [];
        InsertData(empty, element);
        empty[0] = (empty[0] * size) + reposX ;
        empty[1] -= 0.001;   // ensure that outer is lower.
        empty[2] = (empty[2] * size) + reposZ ;
        // add color (random)
        InsertData(empty, petalcolor);
        // add to outer
        InsertData(flowerOuter, empty);
        flowerOuter.push(1);
        i++;
    });
    
    return [flowerOuter, flowerInner];
    
}

